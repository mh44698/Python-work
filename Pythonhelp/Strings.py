#Get Positional Character
a = "Hello, World!"
print(a[1])
#Substring Get the Characters from Position 2 to 5, not including 5
b = "Hello, World!"
print(b[2:5])
#Remove WhiteSpace from beginning or end
a = " Hello, World! "
print(a.strip())
#Display length of a string
a = "Hello, World!"
print(len(a))
#Convert string to Lower case
a = "Hello, World!"
print(a.lower())
#Convert string to Upper case
a = "Hello, World!"
print(a.upper())
#Replace a string with a string
a = "Hello, World!"
print(a.replace("H", "J"))
#split a string into substrings
a = "Hello, World!"
b = a.split(",")
print(b)












